export type ErrorCode = (typeof ErrorCodes)[keyof typeof ErrorCodes];

// ── Standard error response ──

export interface ApiErrorResponse {
  error: string;
  code: ErrorCode;
  details?: string;
  timestamp: string;
}

/**
 * Creates a standardized JSON error response.
 */
export function apiError(
  code: ErrorCode,
  message: string,
  status: number,
  details?: string
): NextResponse<ApiErrorResponse> {
  return NextResponse.json(
    {
      error: message,
      code,
      details,
      timestamp: new Date().toISOString(),
    },
    { status }
  );
}

/**
 * Wraps an async route handler with consistent error catching.
 * Catches unhandled errors and returns a standardized 500 response.
 */
export function withErrorHandler(
  handler: (req: any) => Promise<NextResponse>
) {
  return async (req: Request): Promise<NextResponse> => {
    try {
      return await handler(req);
    } catch (error: unknown) {
      const message = error instanceof Error ? error.message : "Internal server error";
      console.error(`[VIXUAL API ERROR] ${message}`, error);
      return apiError(ErrorCodes.ERR_INTERNAL, message, 500);
    }
  };
}
